from flask import Flask, request, jsonify
import requests
import random

app = Flask(__name__)

KEYS = {
    "EvoAyush": {"limit": float('inf'), "used": 0},
    "Ayush": {"limit": 50, "used": 0}, 
    "Evo": {"limit": 10, "used": 0}
}

BMW_URL = "http://187.127.175.208:5000/Bmw"

def get_nested(data, *keys):
    for key in keys:
        if isinstance(data, dict) and key in data:
            data = data[key]
        else:
            return None
    return data

@app.route('/like', methods=['GET'])
def like():
    uid = request.args.get('uid')
    region = request.args.get('region', '').upper()
    key = request.args.get('key')
    
    if not all([uid, region, key]):
        return jsonify({"status": "errors", "message": "Missing uid, region or key"}), 400
    
    if region!= "IND":
        return jsonify({"status": "failed", "message": "Only IND region allowed"}), 403
        
    if key not in KEYS:
        return jsonify({"status": "failed", "message": "Invalid key"}), 403
    
    key_info = KEYS[key]
    if key_info["used"] >= key_info["limit"]:
        limit = int(key_info['limit']) if key_info['limit']!= float('inf') else 'Unlimited'
        return jsonify({
            "status": "failed", 
            "message": "Key limit exceeded",
            "remains": f"(0/{limit})"
        }), 429
    
    try:
        api_url = f"{BMW_URL}?uid={uid}&region={region}"
        resp = requests.get(api_url, timeout=8).json()
        
        current_likes = int(get_nested(resp, "basic_info", "liked") or 0) # Ye actual likes hai
        nickname = str(get_nested(resp, "basic_info", "nickname") or "Unknown")
        user_uid = int(get_nested(resp, "basic_info", "account_id") or uid)
        
        player_region = get_nested(resp, "basic_info", "region")
        if player_region and player_region.upper()!= "IND":
            return jsonify({"status": "failed", "message": "Player not in IND region"}), 403
            
    except Exception as e:
        return jsonify({"status": "errors", "message": f"BMW API Error: {str(e)}"}), 500
    
    # Main Logic Change Yaha Hai
    likes_given = random.randint(210, 222)
    likes_after = current_likes # After me actual current likes dikhenge
    likes_before = current_likes - likes_given # Before = Current - Given
    
    # Agar current likes given se kam hai to negative na ho
    if likes_before < 0:
        likes_before = 0
        likes_after = likes_given
    
    KEYS[key]["used"] += 1
    
    if key_info["limit"] == float('inf'):
        remains = "Unlimited"
    else:
        remaining = int(key_info["limit"] - key_info["used"])
        total = int(key_info["limit"])
        remains = f"({remaining}/{total})"
    
    return jsonify({
        "LikesGivenByAPI": likes_given,
        "LikesafterCommand": likes_after, # Ab ye BMW wale current likes hai
        "LikesbeforeCommand": likes_before, # Ye = current - given
        "PlayerNickname": nickname,
        "UID": user_uid,
        "remains": remains,
        "status": 1
    })

